let lang = (() => {
  try { const s = JSON.parse(localStorage.getItem('wearclothing.lang')); if (s === 'de' || s === 'en') return s; } catch {}
  return (navigator.language || 'de').toLowerCase().startsWith('en') ? 'en' : 'de';
})();

export function getLang() { return lang; }
export function setLang(next) {
  lang = next === 'en' ? 'en' : 'de';
  try { localStorage.setItem('wearclothing.lang', JSON.stringify(lang)); } catch {}
}
export function t(key, ...args) {
  const entry = STRINGS[lang][key] ?? STRINGS.de[key] ?? key;
  return typeof entry === 'function' ? entry(...args) : entry;
}
// Statische Elemente mit data-i18n / data-i18n-ph / data-i18n-aria füllen
export function applyStaticTranslations(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.setAttribute('placeholder', t(el.dataset.i18nPh)); });
  root.querySelectorAll('[data-i18n-aria]').forEach((el) => { el.setAttribute('aria-label', t(el.dataset.i18nAria)); });
  document.documentElement.lang = lang;
}
